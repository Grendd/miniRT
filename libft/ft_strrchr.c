#include "includes/libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int		i;

	i = 0;
	while (str[i])
		i++;
	i++;
	while (i--)
		if (str[i] == (char)c)
			return ((char *)str + i);
	return (NULL);
}
